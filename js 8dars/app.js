function rendercard(rendered) {
  container.innerHTML = "";

  rendered.map((item) => {
    let card = document.createElement("div");
    card.classList.add("box");
    card.innerHTML = `
  <img src="${item.avatar}"/>
  <p>${item.brand}</p>
  <p>${item.price}</p>
  
  <button>Add</button>
  
  `;
    container.appendChild(card);
  });
}
